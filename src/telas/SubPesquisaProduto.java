    private void CarregaProduto(){

        String SQL;

        SQL = "select tpr.codigo, tpr.codigo_fornecedor, tpr.nome, tpr.qtde from t_produto tpr ";
        
        if (jrbNomeProduto.isSelected()){
            SQL = SQL + "where UPPER(tpr.nome) like '%" + jtfPesquisa.getText().toUpperCase() + "%' and tpr.exclusao = 0 ";
        } else if (jrbCodigoFabricante.isSelected()) {
            SQL = SQL + "where UPPER(tpr.codigo_fornecedor) like '%" + jtfPesquisa.getText().toUpperCase() + "%' and tpr.exclusao = 0 ";
        } else if (jrbCodigoSistema.isSelected()){
            SQL = SQL + "where tpr.codigo = " + jtfPesquisa.getText() + " and tpr.exclusao = 0 ";
        } else if (jrbFabricanteProduto.isSelected()){
            SQL = SQL + "inner join t_pessoa tp on tp.codigo = tpr.codigo_cad_fornecedor ";
            SQL = SQL + "where UPPER(tp.nome) like '%" + jtfPesquisa.getText().toUpperCase() + "%' and tpr.exclusao = 0 ";
        }
        
        SQL = SQL + "order by tpr.codigo ";

        ((DefaultTableModel) jTable1.getModel()).setNumRows(0);
        jTable1.updateUI();

        Conexao = new ConexaoPostgresql();
        Conexao.Conectar();
        Conexao.setSQL(SQL);

        ResultSet RS = Conexao.Consultar();

        try{

            while (RS.next()){
                DefaultTableModel Tabela = (DefaultTableModel) jTable1.getModel();
                Object[] CarLinha = {RS.getString("codigo"),RS.getString("codigo_fornecedor"),RS.getString("nome"),RS.getString("qtde")};
                Tabela.addRow(CarLinha);
            }

        }catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na valida莽茫o: " + Erro);
        }

        Conexao.Desconectar();

    }

