    public void centralizar(JDesktopPane dp) {
        Dimension ds = dp.getSize();
        Dimension dw = this.getSize();
        this.setLocation((ds.width - dw.width) / 2, (ds.height - dw.height) / 2);
    }
    
    private void Limpar (){
        
        jtfCodigo.setText("");
        jtfNome.setText("");
        jtfSigla.setText("");
        
    }

    public void CarregaDados(int Codigo){

        String SQL;
        SQL = "select * from t_estado where codigo = " + Codigo + " and exclusao = 0";
        Conexao = new ConexaoPostgresql();
        Conexao.Conectar() ;
        Conexao.setSQL(SQL);

        ResultSet RS = Conexao.Consultar();

        try{
            if (RS.next()){
                jtfCodigo.setText(RS.getString("codigo"));
                jtfNome.setText(RS.getString("nome"));
                jtfSigla.setText(RS.getString("sigla"));
            }else{
                Limpar();

                jtfNome.requestFocus();
            }
        }catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na validaÃ§Ã£o" + Erro);
        }
        jtfNome.requestFocus();
        Conexao.Desconectar();

    }


