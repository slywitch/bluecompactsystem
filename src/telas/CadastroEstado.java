    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed

        String SQL;
        
        if (jtfCodigo.getText().equals("")){
            Fun = new Funcoes();
            jtfCodigo.setText(Fun.Proximo("codigo", "t_estado", ""));
        }

        Conexao = new ConexaoPostgresql();
        Conexao.Conectar();
        SQL = "select * from t_estado where codigo = " + jtfCodigo.getText() + " and exclusao = 0";
        Conexao.setSQL(SQL);
        ResultSet RS = Conexao.Consultar();

        try {
            if(RS.next()){                
                Conexao.setSQL("update t_estado set nome = '" + jtfNome.getText() + "', sigla = '" + jtfSigla.getText() + "' where codigo = " + jtfCodigo.getText());
                Conexao.Executar();
            } else {
                Conexao.setSQL("insert into t_estado (codigo, nome, sigla, exclusao) values(" + jtfCodigo.getText() + ", '" + jtfNome.getText() + "', '" + jtfSigla.getText() + "', 0)");
                Conexao.Executar();
            }

        } catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na validaÃ§Ã£o: " + Erro);
        }
        
        Conexao.Desconectar();
        
        Limpar();
        
        jtfNome.requestFocus();        

    }//GEN-LAST:event_jbtSalvarActionPerformed

